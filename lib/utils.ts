export function getEventTypeName(eventType: string): string {
  // eventType might be in the form "EventType1", "EventType2", etc.
  if (eventType.startsWith('EventType')) {
    const id = parseInt(eventType.substring(9), 10); // Extract the number after "EventType"
    return EVENT_TYPE_MAP[id] || eventType; // Return the mapped name or the original if not found
  }
  return eventType;
}
